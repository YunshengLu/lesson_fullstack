/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */

/**
 * @param {ListNode} headA
 * @param {ListNode} headB
 * @return {ListNode}
 */
var getIntersectionNode = function (headA, headB) {
    let n1 = headA;
    let n2 = headB;

    // 遍历判断两指针是否相等
    while (n1 !== n2) {
        // 若是指针走到了链表结尾都没相交，那么n1指向另一条链表
        if (n1 !== null) {
            n1 = n1.next;
        } else {
            n1 = headB;
        }

        if (n2 !== null) {
            n2 = n2.next;
        } else {
            n2 = headA;
        }
    }

    // 返回相交的节点
    return n1;
};
